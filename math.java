public class math {
    public static void main(String[] args) {
        int a = -25;
        int b = 2;
        int c = 3;
        int x = 20;
        int y = 10;
        double z = 10.4;

        System.out.println("Maximum number: " + Math.max(x,y));
        System.out.println("Minimum number: " + Math.min(x,y));
        System.out.println("Absolute number: " + Math.abs(a));
        System.out.println("Power: " + Math.pow(b,c));
        System.out.println("Round: " + Math.round(z));
        System.out.println("PI: " + Math.PI);

    }
}
